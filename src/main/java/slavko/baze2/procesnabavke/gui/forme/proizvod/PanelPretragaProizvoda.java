    private void jbtnPronadjiProizvodeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnPronadjiProizvodeActionPerformed
        String naziv = jtxtNazivProizvoda.getText();
        String minCena = jftxtMinCena.getText();
        String maxCena = jftfMaxCena.getText();
        String stanjeIspod = jftfStanjeIspod.getText();

        Double minCenaDouble = null;
        if (minCena != null && !minCena.isEmpty()) {
            minCenaDouble = Double.parseDouble(minCena.replace(",", ""));
        }
        Double maxCenaDouble = null;
        if (maxCena != null && !maxCena.isEmpty()) {
            maxCenaDouble = Double.parseDouble(maxCena.replace(",", ""));
        }
        Double stanjeIspodDouble = null;
        if (stanjeIspod != null && !stanjeIspod.isEmpty()) {
            stanjeIspodDouble = Double.parseDouble(stanjeIspod.replace(",", ""));
        }

        try {
            List<Proizvod> sviProizvodi = new ArrayList<>();
            List<Proizvod> filtriraniProizvodi = new ArrayList<>();
            sviProizvodi = Kontroler.vratiInstancu().vratiProizvode();
            for (Proizvod p : sviProizvodi) {
                if (naziv != null && !naziv.isEmpty()) {
                    if (!p.getNaziv().contains(naziv)) {
                        continue;
                    }
                }
                if (minCenaDouble != null) {
                    if (!(p.getCenaBezPDVa() >= minCenaDouble)) {
                        continue;
                    }
                }
                if (maxCenaDouble != null) {
                    if (!(p.getCenaBezPDVa() <= maxCenaDouble)) {
                        continue;
                    }
                }
                if (stanjeIspodDouble != null) {
                    if (!(p.getStanje() < stanjeIspodDouble)) {
                        continue;
                    }
                }
                filtriraniProizvodi.add(p);
            }
            ProizvodTableModel ptm = ((ProizvodTableModel) jtblProizvodi.getModel());
            ptm.postaviProizvode(filtriraniProizvodi);
            if(filtriraniProizvodi.isEmpty()){
                JOptionPane.showMessageDialog(this, "Sistem ne može da nadje proizvode po zadatim vrednostima", "Greška", JOptionPane.ERROR_MESSAGE);
            }

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Greška", JOptionPane.ERROR_MESSAGE);
        }

        jftfMaxCena.setValue(null);
        jftxtMinCena.setValue(null);
        jftfStanjeIspod.setValue(null);


    }//GEN-LAST:event_jbtnPronadjiProizvodeActionPerformed

    private void jbtnDetaljiProizvodActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtnDetaljiProizvodActionPerformed
         if (jtblProizvodi.getSelectedRow() < 0) {
            JOptionPane.showMessageDialog(this, "Morate selektovati proizvod u tabeli!", "Greška", JOptionPane.ERROR_MESSAGE);
            return;
        }
        slavko.baze2.procesnabavke.gui.util.Util.vratiInstancu().postavi("izabrani_proizvod", ((ProizvodTableModel) jtblProizvodi.getModel()).getProizvod(jtblProizvodi.getSelectedRow()));
        JDialog dialog = new JDialog((JDialog) SwingUtilities.getWindowAncestor(this), "Izmena proizvoda", true);
        dialog.setLayout(new BorderLayout());
        dialog.add(new PanelRadSaProizvodom(), BorderLayout.CENTER);
        dialog.pack();
        dialog.setLocationRelativeTo(this);
        dialog.setVisible(true);
    }//GEN-LAST:event_jbtnDetaljiProizvodActionPerformed

