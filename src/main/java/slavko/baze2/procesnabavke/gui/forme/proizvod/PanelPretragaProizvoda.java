    private void srediFormu(boolean samoIzbor) {
        if (samoIzbor) {
            jbtnDetaljiProizvod.setVisible(false);
            jbtnObrisiProizvod.setVisible(false);
        } else {
            jbtnIzaberi.setVisible(false);
            jftxtKoličina.setVisible(false);
            jlblKoličina.setVisible(false);
        }

        NumberFormatter formatter = new NumberFormatter(NumberFormat.getInstance());

        formatter.setMinimum(1);
        formatter.setMaximum(10000000);
        formatter.setAllowsInvalid(false);

        jftxtMinCena.setFormatterFactory(new DefaultFormatterFactory(formatter));
        jftfMaxCena.setFormatterFactory(new DefaultFormatterFactory(formatter));
        jftfStanjeIspod.setFormatterFactory(new DefaultFormatterFactory(formatter));
        jftxtKoličina.setFormatterFactory(new DefaultFormatterFactory(formatter));
        jftxtKoličina.setValue(1);

        List<Proizvod> lp = new ArrayList<>();
        try {
            lp = Kontroler.vratiInstancu().vratiProizvode();
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(this, ex.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
        jtblProizvodi.setModel(new ProizvodTableModel(lp));
        GuiUtil.resizeColumnWidth(jtblProizvodi,50);
        jtblProizvodi.setAutoResizeMode(JTable.AUTO_RESIZE_OFF);
    }

    

